/**
 * Global helper to get a localized label.
 */
window.getLabel = function(key, fallback = '') {
  const locale = window.currentTeachingLocale ? window.currentTeachingLocale() : 'zh_tw';
  const labels = window.I18N_LABELS[locale] || window.I18N_LABELS.zh_tw;
  return labels[key] || fallback || key;
};
